    public void pushtecla(String letra, JTextField txt ){
   
        String aux = txt.getText();              
        int pos=txt.getCaretPosition();
        String texto= aux.substring(0, pos)+letra+aux.substring(pos, aux.length());
        txt.setText(texto);
        txt.setCaretPosition(pos+1);
    }
    
